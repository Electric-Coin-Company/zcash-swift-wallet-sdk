import XCTest
@testable import ZcashLightClientKit

class Zip302MemoTests: XCTestCase {

    /// Zip-302 specifies "the byte 0xF6 followed by 511 0x00 bytes, indicating 'no memo'"
    func testEmptyMemoBytesHasF6LeadByteAndThenZeroes() throws {
        XCTAssertEqual(MemoBytes.empty().bytes, Self.emptyMemoBytes)
    }

    /// ZIP-302 specifies that all memos must have 512 bytes. When `MemoBytes` is initialized with
    /// less bytes it should append trailing zeroes until it reaches the capacity.
    func testItShouldAppendTrailingZeroesWhenBytesAreSmallerThanCapacity() throws {
        let shortBytes: [UInt8] = [0x56, 0x17, 0xe0, 0xac, 0x3c, 0xbc, 0xde]

        XCTAssertEqual(try MemoBytes(bytes: shortBytes).bytes, Self.shortButPaddedBytes)

        XCTAssertEqual(try MemoBytes(bytes: Self.fullMemoBytes).bytes, Self.fullMemoBytes)

        XCTAssertEqual(try MemoBytes(bytes: Self.almostFullMemoBytes).bytes, Self.almostFullMemoBytes)
    }

    func testItConvertsIntoAnEmptyMemoWhenFirstByteIsF6AndTrailingBytesAreAllZeros() throws {
        let memoBytes = try MemoBytes(bytes: .emptyMemoBytes)

        XCTAssertEqual(try memoBytes.intoMemo(), .empty)
    }

    func testItConvertsIntoATextMemoWhenFirstByteIsF4OrSmaller() throws {
        let textMemoBytes = try MemoBytes(bytes: Self.helloImATextMemo)

        let memo = try textMemoBytes.intoMemo()

        XCTAssertEqual(memo, Memo.text(try MemoText("Hello I'm a Text Memo")))
    }

    func testItConvertsIntoFutureMemoWhenFirstByteIsF5() throws {
        let futureMemo = try MemoBytes(bytes: Self.memoFromTheFutureWithF5)

        let memo = try futureMemo.intoMemo()

        XCTAssertEqual(memo, .future(futureMemo))
    }

    func testItConvertsInFutureMemoWhenFirstByteIsF6AndTrailingBytesAreNotNull() throws {
        let futureMemoF6AndNotNil = try MemoBytes(bytes: Self.memoFromTheFutureSimilarToAnEmptyMemo)

        let memo = try futureMemoF6AndNotNil.intoMemo()

        XCTAssertEqual(memo, .future(futureMemoF6AndNotNil))
    }

    func testItConvertsInFutureMemoWhenFirstByteIsBetweenF7andFEInclusive() throws {
        var memoFromTheFuture = Self.memoFromTheFutureWithF7

        for firstByte in UInt8(0xF7) ... 0xFE {
            memoFromTheFuture[0] = firstByte

            let futureMemoBytes = try MemoBytes(bytes: memoFromTheFuture)

            let memo = try futureMemoBytes.intoMemo()

            XCTAssertEqual(
                memo,
                .future(futureMemoBytes),
                "Failed to create a Future memo starting with \(String(format:"%02X", firstByte))"
            )
        }
    }

    func testItConvertsIntoArbitraryMemoWhenFirstByteIsFF() throws {
        let arbitraryMemoBytes = try MemoBytes(bytes: Self.arbitraryMemo)

        let arbitraryMemo = try arbitraryMemoBytes.intoMemo()

        XCTAssertEqual(arbitraryMemo, .arbitrary([UInt8](Self.arbitraryMemo.dropFirst())))
    }

    func testItThrowsTooLongWhenCreatingAMemoFromAValidButTooLongString() throws {
        let tooLongString = "thiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis iiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa veeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeryyyyyyyyyyyyyyyyyyyyyyyyyy looooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooong meeeeeeeeeeeeeeeeeeemooooooooooooooooooooooooooooooooooooooooooooooooooooooooooo but it's now a bit too long"

        XCTAssertThrowsError(try Memo(string: tooLongString)) { err in
            guard let error = err as? MemoBytes.Errors else {
                XCTFail("Expected `MemoBytes.Errors.TooLong` error but found \(err.localizedDescription)")
                return
            }

            switch error {
                case .tooLong(let length) :
                    XCTAssertEqual(length, 513)

                default:
                    XCTFail("Expected `MemoBytes.Errors.TooLong` error but found \(err.localizedDescription)")
            }
        }
    }

    func testItCreatesAMemoFromAValidAndShortEnoughText() throws {
        let almostTooLongString = "thiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis iiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa veeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeryyyyyyyyyyyyyyyyyyyyyyyyyy looooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooong meeeeeeeeeeeeeeeeeeemooooooooooooooooooooooooooooooooooooooooooooooooooooooooooo but it's just short enough"

        let memo = try Memo(string: almostTooLongString)

        let memoBytes = try memo.asMemoBytes()

        XCTAssertEqual(memoBytes.bytes, Self.almostTooLongStringBytes)
    }

    func testItThrowsTooLongWhenMemoCharacterCountIsLongEnoughButIsTooLongInBytes() {
        /// This string's count it 512 characters, but when converted to UTF8 bytes, it has 515 bytes.
        let almostTooLongString = "thiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis iiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa veeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeryyyyyyyyyyyyyyyyyyyyyyyyyy looooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooong meeeeeeeeeeeeeeeeeeemooooooooooooooooooooooooooooooooooooooooooooooooooooooooooo but it's just short is it😛"
        
        XCTAssertThrowsError(try Memo(string: almostTooLongString)) { err in
            guard let error = err as? MemoBytes.Errors else {
                XCTFail("Expected `MemoBytes.Errors kind of error but found \(err)")
                return
            }

            switch error {
            case .tooLong(let count):
                XCTAssertEqual(count, 515)
            case .invalidUTF8:
                XCTFail("Expected `.tooLong(515) but found `.invalidUTF8`")
            case .endsWithNullBytes:
                XCTFail("Expected `.tooLong(515) but found `.endsWithNullBytes`")
            }
        }
    }

    func testInitMemoBytesFromContiguousBytes() throws {
        let contiguousEmptyBytes = ContiguousArray<UInt8>(Zip302MemoTests.emptyMemoBytes)

        let emptyMemoBytes = try MemoBytes(contiguousBytes: contiguousEmptyBytes)

        XCTAssertEqual(emptyMemoBytes.bytes, .emptyMemoBytes)

        let contiguousTextMemoBytes = ContiguousArray<UInt8>(Zip302MemoTests.helloImATextMemo)

        let textMemoBytes = try MemoBytes(contiguousBytes: contiguousTextMemoBytes)

        XCTAssertEqual(textMemoBytes.bytes, Zip302MemoTests.helloImATextMemo)
    }

    func testThrowsWhenTextMemoIsConstructedWithTrailingNullBytes() throws {
        let nullTrailedString = "This Is a memo with text and trailing null bytes\u{0}\u{0}\u{0}\u{0}\u{0}\u{0}\u{0}"

        XCTAssertThrowsError(try Memo(string: nullTrailedString)) { error in
            guard let thrownError = error as? MemoBytes.Errors else {
                XCTFail("Thrown erros is not MemoBytes.Error")
                return
            }

            switch thrownError {
            case .invalidUTF8, .tooLong:
                XCTFail("Expected .endsWithNullBytes found other errors")
            case .endsWithNullBytes:
                return
            }
        }
    }

    func testThrowsWhenTextMemoIsConstructedWithNullBytes() throws {
        let nullTrailedString = "\u{0}"

        XCTAssertThrowsError(try Memo(string: nullTrailedString)) { error in
            guard let thrownError = error as? MemoBytes.Errors else {
                XCTFail("Thrown erros is not MemoBytes.Error")
                return
            }

            switch thrownError {
            case .invalidUTF8, .tooLong:
                XCTFail("Expected .endsWithNullBytes found other errors")
            case .endsWithNullBytes:
                return
            }
        }
    }

    func testTextMemoIsConstructedWithLeadingNullBytes() throws {
        let nullLedString = "\u{0}ABC"

        let nullLedTextMemo = try MemoText(nullLedString)

        let nullLedMemo = try Memo(string: nullLedString)

        if case .text(let textMemo) = nullLedMemo {
            XCTAssertEqual(nullLedTextMemo, textMemo)
        } else {
            XCTFail("Expected a TextMemo")
        }
    }

    func testTextMemoIsConstructedWithEmptyString() throws {
        let emptyString = ""

        let emptyTextMemo = try MemoText(emptyString)

        let emptyStringMemo = try Memo(string: emptyString)

        if case .text(let textMemo) = emptyStringMemo {
            XCTAssertEqual(emptyTextMemo, textMemo)
        } else {
            XCTFail("Expected a TextMemo")
        }
    }

    func testUnpaddedRawBytesWhenPaddingIsFound() throws {
        let expected: [UInt8] = [0x56, 0x17, 0xe0, 0xac, 0x3c, 0xbc, 0xde]

        XCTAssertEqual(Zip302MemoTests.shortButPaddedBytes.unpaddedRawBytes(), expected)
    }

    func testUnpaddedRawBytesWhenThereIsNoPadding() throws {
        XCTAssertEqual(Self.fullMemoBytes.unpaddedRawBytes(), Self.fullMemoBytes)
    }

    func testMemoLenght1CharTooLong () {
        let tooLongString = "thiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis iiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa veeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeryyyyyyyyyyyyyyyyyyyyyyyyyy looooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooong meeeeeeeeeeeeeeeeeeemooooooooooooooooooooooooooooooooooooooooooooooooooooooooooo but it's now a bit too long"

        XCTAssertEqual(Memo.length(for: tooLongString), 513)
    }

    func testTextIsVisibleFitButExceedsLimit() {
        let tooLongStringThatVisiblyFits = "thiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis iiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiiis aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa veeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeeryyyyyyyyyyyyyyyyyyyyyyyyyy looooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooooong meeeeeeeeeeeeeeeeemooooooooooooooooooooooooooooooooooooooong. Visibly fit, but it exceeds the byte limit. 👩‍👩‍👧‍👧"
        XCTAssertEqual(MemoBytes.capacity, tooLongStringThatVisiblyFits.count) // this assertion proves that the count of visible characters is within the limit
        XCTAssertTrue(Memo.length(for: tooLongStringThatVisiblyFits) > MemoBytes.capacity)

        XCTAssertEqual(Memo.length(for: tooLongStringThatVisiblyFits), 536)
    }
}


